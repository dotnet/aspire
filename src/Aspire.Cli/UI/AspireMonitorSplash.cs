/// <summary>
/// Animated splash screen that renders the Aspire logo using half-block characters.
/// Pixels fly in from random off-screen positions to their final locations.
/// </summary>
internal sealed class AspireMonitorSplash
{
    public const int SplashDurationMs = 2000;

    // Logo dimensions in logical pixels (each pixel is half a character cell vertically)
    private const int LogoWidth = 40;
    private const int LogoHeight = 40;
    private const int LogoCellHeight = LogoHeight / 2;
    public const int AnimationDurationMs = 1200;

    private readonly record struct PixelState(
        int FinalX, int FinalY, byte R, byte G, byte B,
        float StartOffsetX, float StartOffsetY);

    private readonly PixelState[] _pixels;
    private readonly long _startTicks;

    public AspireMonitorSplash()
    {
        _startTicks = Environment.TickCount64;
        var rng = new Random(42); // Fixed seed for deterministic animation

        var pixelCount = s_pixelData.Length / 5;
        _pixels = new PixelState[pixelCount];

        for (var i = 0; i < pixelCount; i++)
        {
            var offset = i * 5;
            var x = s_pixelData[offset];
            var y = s_pixelData[offset + 1];
            var r = s_pixelData[offset + 2];
            var g = s_pixelData[offset + 3];
            var b = s_pixelData[offset + 4];

            // Random start direction — fly in from outside the logo bounds
            var angle = rng.NextDouble() * Math.PI * 2;
            var distance = 30 + rng.NextDouble() * 40;
            var startOffsetX = (float)(Math.Cos(angle) * distance);
            var startOffsetY = (float)(Math.Sin(angle) * distance);

            _pixels[i] = new PixelState(x, y, r, g, b, startOffsetX, startOffsetY);
        }
    }

    /// <summary>
    /// Gets the animation progress from 0.0 to 1.0.
    /// </summary>
    public float Progress
    {
        get
        {
            var elapsed = Environment.TickCount64 - _startTicks;
            return Math.Clamp(elapsed / (float)AnimationDurationMs, 0f, 1f);
        }
    }

    public bool IsComplete => Progress >= 1f;

    public Hex1bWidget Build(RootContext ctx)
    {
        var progress = Progress;

        // Ease-out cubic for smooth deceleration
        var t = 1f - (1f - progress) * (1f - progress) * (1f - progress);

        return ctx.ThemePanel(AspireTheme.Apply, ctx.VStack(outer => [
            outer.Text("").Fill(),
            outer.Center(
                ctx.Surface(layerCtx => [
                    layerCtx.Layer(surface =>
                    {
                        RenderFrame(surface, t);
                    })
                ]).Size(LogoWidth, LogoCellHeight)
            ),
            outer.Text("").Fill()
        ]).Fill()).Fill();
    }

    private void RenderFrame(Hex1b.Surfaces.Surface surface, float t)
    {
        // Build a pixel grid: for each cell (cx, cy), we need the top and bottom logical pixel
        // Top pixel = logical row cy*2, bottom pixel = logical row cy*2+1
        // Use ▀ with fg=top color, bg=bottom color

        // Collect animated pixel positions into a sparse grid
        var grid = new (byte R, byte G, byte B, bool HasPixel)[LogoWidth, LogoHeight];

        foreach (var pixel in _pixels)
        {
            // Lerp from start to final position
            var currentX = pixel.FinalX + pixel.StartOffsetX * (1f - t);
            var currentY = pixel.FinalY + pixel.StartOffsetY * (1f - t);

            var ix = (int)Math.Round(currentX);
            var iy = (int)Math.Round(currentY);

            if (ix >= 0 && ix < LogoWidth && iy >= 0 && iy < LogoHeight)
            {
                grid[ix, iy] = (pixel.R, pixel.G, pixel.B, true);
            }
        }

        // Render half-block pairs
        for (var cy = 0; cy < LogoCellHeight && cy < surface.Height; cy++)
        {
            for (var cx = 0; cx < LogoWidth && cx < surface.Width; cx++)
            {
                var topRow = cy * 2;
                var botRow = cy * 2 + 1;

                var top = grid[cx, topRow];
                var bot = botRow < LogoHeight ? grid[cx, botRow] : default;

                if (top.HasPixel && bot.HasPixel)
                {
                    // Both pixels visible: ▀ with fg=top, bg=bottom
                    var fg = Hex1bColor.FromRgb(top.R, top.G, top.B);
                    var bg = Hex1bColor.FromRgb(bot.R, bot.G, bot.B);
                    surface.WriteChar(cx, cy, '\u2580', fg, bg);
                }
                else if (top.HasPixel)
                {
                    // Only top pixel: ▀ with fg=top color
                    var fg = Hex1bColor.FromRgb(top.R, top.G, top.B);
                    surface.WriteChar(cx, cy, '\u2580', fg);
                }
                else if (bot.HasPixel)
                {
                    // Only bottom pixel: ▄ with fg=bottom color
                    var fg = Hex1bColor.FromRgb(bot.R, bot.G, bot.B);
                    surface.WriteChar(cx, cy, '\u2584', fg);
                }
            }
        }
    }

